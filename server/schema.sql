
CREATE TABLE items (
    id INT,
    name varchar(255),
    description varchar(255),
    price INT,
    inventory INT,
    image varchar(255),
    category varchar(255),
    material_base varchar(80),
    material_etc varchar(80),
    material_plated varchar(80),
    material_stone varchar(80),
    shape varchar(80),
    size varchar(80),
    weight varchar(80),
    PRIMARY KEY (id)

)